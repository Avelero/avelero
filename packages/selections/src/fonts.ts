/**
 * Export all font families as a simple string array for easy selection
 */
export const fontFamilies = fonts.map((font) => font.family);

/**
 * Find font metadata by family name (case-insensitive)
 */
export function findFont(familyName: string): FontMetadata | undefined {
  return fonts.find(
    (font) => font.family.toLowerCase() === familyName.toLowerCase(),
  );
}

/**
 * Get fallback font category for a given font
 */
export function getFontFallback(familyName: string): string {
  const font = findFont(familyName);
  if (font) {
    // Map non-CSS generic names to valid CSS generic-family names
    switch (font.category) {
      case "display":
        return "sans-serif"; // or 'system-ui' for UI fonts
      case "handwriting":
        return "cursive";
      case "monospace":
      case "serif":
        return font.category; // These are already valid CSS generic-family names
      default:
        return "sans-serif";
    }
  }
  // Heuristic fallback
  const lower = familyName.toLowerCase();
  if (lower.includes("serif") && !lower.includes("sans")) return "serif";
  if (lower.includes("mono") || lower.includes("code")) return "monospace";
  if (lower.includes("display")) return "sans-serif"; // Map display to sans-serif
  if (lower.includes("script") || lower.includes("cursive")) return "cursive"; // Map handwriting to cursive
  return "sans-serif";
}

/**
 * Get fonts by category
 */
export function getFontsByCategory(
  category: FontMetadata["category"],
): FontMetadata[] {
  return fonts.filter((font) => font.category === category);
}

/**
 * Get variable fonts only
 */
export function getVariableFonts(): FontMetadata[] {
  return fonts.filter((font) => font.isVariable);
}

/**
 * Get static fonts only
 */
export function getStaticFonts(): FontMetadata[] {
  return fonts.filter((font) => !font.isVariable);
}

/**
 * Search fonts by name (case-insensitive)
 */
export function searchFonts(query: string): FontMetadata[] {
  const lowerQuery = query.toLowerCase();
  return fonts.filter((font) => font.family.toLowerCase().includes(lowerQuery));
}
